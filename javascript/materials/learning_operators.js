// Logical or Boolean
console.log("5 == '5' is", 5 == '5');
console.log("5 === '5' is", 5 === '5');

console.log("5 === 5 is", 5 === 5);
console.log("5 === 6 is", 5 === 6);

console.log("5 !== 5 is", 5 !== 5);
console.log("5 !== 6 is", 5 !== 6);

console.log("3 > 2 is", 3 > 2);
console.log("3 < 2 is", 3 < 2);

console.log("3 <= 2 is", 3 <= 2);
console.log("2 <= 2 is", 2 <= 2);

